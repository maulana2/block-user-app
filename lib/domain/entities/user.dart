class User {
  final String id;
  final String name;
  final String email;
  final String address;
  final String phoneNumber;
  final String city;
  final String profileImageUrl;

  User({
    required this.id,
    required this.name,
    required this.email,
    required this.address,
    required this.phoneNumber,
    required this.city,
    required this.profileImageUrl,
  });
}
